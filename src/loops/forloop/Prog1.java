package loops.forloop;

public class Prog1
{
    public static void main(String[] args)
    {
        int i;
        int j;
        for (i=1;i<=10;i++)
        {
            System.out.println(i);
        }

        for (j=10;j>=1;j--)
        {
            System.out.println(j);
        }

    }
}
